def process_robot_command(line, robot):
    """
    Обрабатывает строку команды управления роботом.

    Если строка соответствует одной из поддерживаемых команд (влево, вправо, вверх, вниз, закрасить),
    вызывается соответствующая функция объекта робота для выполнения действия.

    Параметры:
      line (str): Строка команды, введенная пользователем.
      robot (object): Объект робота, который должен иметь методы:
                      - go_left: переместиться влево,
                      - go_right: переместиться вправо,
                      - go_up: переместиться вверх,
                      - go_down: переместиться вниз,
                      - do_paint: выполнить операцию закрашивания клетки.

    Возвращаемое значение:
      bool: True, если строка распознана как команда робота и обработана (вне зависимости от успешности выполнения),
            False, если строка не соответствует ни одной из поддерживаемых команд.

    Исключения:
      Ошибки при выполнении команды перехватываются, и выводится сообщение об ошибке, но исключение не пробрасывается дальше.
    """
    # Приводим команду к нижнему регистру и убираем лишние пробелы
    cmd = line.lower().strip()

    # Определяем словарь с поддерживаемыми командами робота и соответствующими методами объекта robot
    robot_commands = {"влево": robot.go_left, "вправо": robot.go_right, "вверх": robot.go_up, "вниз": robot.go_down,
        "закрасить": robot.do_paint}

    # Если команда содержится в словаре, выполняем соответствующую функцию
    if cmd in robot_commands:
        try:
            robot_commands[cmd]()  # Вызов метода для выполнения команды
        except Exception as e:
            # При возникновении ошибки выводим сообщение об ошибке
            print(f"Ошибка при выполнении команды '{line}': {e}")
        return True
    # Если команда не распознана, возвращаем False
    return False
