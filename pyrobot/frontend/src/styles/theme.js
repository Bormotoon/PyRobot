/**
 * @file theme.js
 * @description Конфигурация темы Material-UI для симулятора робота.
 * Здесь определяется палитра цветов, глобальные настройки скругления углов и базовая типографика,
 * которые используются во всех компонентах приложения.
 */

import {createTheme} from '@mui/material/styles';

// Создаем тему с использованием функции createTheme из Material-UI.
const theme = createTheme({
	// Определение цветовой палитры, которая используется для оформления компонентов.
	palette: {
		// Основные стандартные цвета для компонентов, использующих color="primary" и color="secondary".
		primary: {
			main: '#2196f3', // Основной синий цвет (раньше примерно соответствовал --button-info-bg).
		}, secondary: {
			main: '#9c27b0', // Фиолетовый цвет, может быть изменен по вкусу.
		},

		// Дополнительные цвета для различных состояний компонентов, таких как успех, ошибка, предупреждение и информация.
		success: {
			main: '#4caf50', // Зеленый цвет для обозначения успешных операций.
		}, error: {
			main: '#f44336', // Красный цвет для обозначения ошибок.
		}, warning: {
			main: '#ff9800', // Оранжевый цвет для предупреждений.
		}, info: {
			main: '#03a9f4', // Голубой цвет для информационных сообщений.
		},

		// Дополнительный набор серых тонов. Можно использовать через palette.grey для оформления.
		grey: {
			500: '#9e9e9e', // Светло-серый оттенок.
			700: '#757575', // Темно-серый оттенок.
		},
	},

	// Глобальные настройки формы (shape) для всех компонентов Material-UI.
	shape: {
		// Задает единое скругление углов для всех компонентов.
		borderRadius: 8,
	},

	// Глобальные настройки типографики.
	typography: {
		// Основной шрифт, используемый в компонентах Material-UI.
		fontFamily: 'Arial, sans-serif',
	},
});

// Экспорт темы для использования в ThemeProvider приложения.
export default theme;
